var n = 0; //Se declara e inicializa nuestra variable n en 0

function sumaUno(){ //Función para sumar 1
    n += 1;         //Suma 1 equivalente a n = n + 1;
    console.log(n);
}

function restaUno(){ //Función para restar 1
    n -= 1;          //Resta 1 equivalente a n = n - 1;
    console.log(n); 
}
